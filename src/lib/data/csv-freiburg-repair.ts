/**
 * Ersetzt typische `?`-Platzhalter in Freiburger CSV-Texten.
 */
export function repairFreiburgBrokenUmlautCsvText(text: string): string {
	if (!text.includes('?')) return text;
	let s = text;
	for (const [re, rep] of PAIRS) {
		s = s.replace(re, rep);
	}
	return s;
}
